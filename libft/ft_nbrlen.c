#include "libft.h"

int	ft_nbrlen(int n)
{
	int		len;
	long	nbr;

	if (n == 0)
		return (1);
	len = 0;
	nbr = n;
	if (n < 0)
	{
		len++;
		nbr *= -1;
	}
	while (nbr)
	{
		nbr /= 10;
		len++;
	}
	return (len);
}
