#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int j;
	int i;

	j = 0;
	i = ft_strlen(s1);
	while (s2[j])
	{
		s1[i + j] = s2[j];
		j++;
	}
	s1[i + j] = '\0';
	return (s1);
}
